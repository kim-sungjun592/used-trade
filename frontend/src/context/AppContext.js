//  AppProvider  (기존 코드 그대로 유지)
// ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
export function AppProvider({ children }) {
  const [currentUser, setCurrentUser] = useState(() => {
    const savedNickname = localStorage.getItem("nickname");
    const savedEmail = localStorage.getItem("email");
    return savedNickname
      ? { name: savedNickname, email: savedEmail || "" }
      : null;
  });

  const [products, setProducts] = useState(mockProducts);
  const [myProducts, setMyProducts] = useState([]);

  const [likedItems, setLikedItems] = useState(() => {
    const saved = localStorage.getItem("local_likes");
    return saved ? JSON.parse(saved) : [];
  });

  const [chatRooms, setChatRooms] = useState([]);
  const [selectedCategory, setSelectedCategory] = useState("전체");
  const [searchQuery, setSearchQuery] = useState("");
  const [regionFilter, setRegionFilter] = useState({
    sido: "전국",
    sigungu: "전체",
    dong: "전체",
  });

  const [barterRequests, setBarterRequests] = useState(() => {
    const saved = localStorage.getItem("local_barters");
    return saved ? JSON.parse(saved) : [];
  });

  const [myPoints] = useState(50000);
  const [isHeaderMenuOpen, setIsHeaderMenuOpen] = useState(false);

  useEffect(() => {
    localStorage.setItem("local_likes", JSON.stringify(likedItems));
  }, [likedItems]);
  useEffect(() => {
    localStorage.setItem("local_barters", JSON.stringify(barterRequests));
  }, [barterRequests]);

  const addProduct = (newProduct) => {
    const productWithId = {
      ...newProduct,
      id: Date.now(),
      date: new Date().toISOString().split("T")[0],
      seller: currentUser?.name || "익명",
      sellerId: currentUser?.id || "guest",
    };
    setMyProducts((prev) => [productWithId, ...prev]);
    setProducts((prev) => [productWithId, ...prev]);
  };

  const toggleLike = async (productId) => {
    const token = localStorage.getItem("token");
    if (!token) {
      alert("로그인이 필요한 기능입니다.");
      return;
    }
    const strId = String(productId);
    setLikedItems((prev) =>
      prev.includes(strId)
        ? prev.filter((id) => id !== strId)
        : [...prev, strId],
    );
    try {
      await fetch("http://localhost:5002/api/wishlist", {
        method: "POST",
        headers: {
          "Content-Type": "application/json",
          Authorization: `Bearer ${token}`,
        },
        body: JSON.stringify({ productId: Number(productId) }),
      });
    } catch {
      console.log("백엔드 반영 안 됨 (로컬 모드 유지)");
    }
  };

  const toggleHeaderMenu = () => setIsHeaderMenuOpen((prev) => !prev);

  const startChat = (product) => {
    const existing = chatRooms.find((r) => r.id === product.id);
    if (existing) return existing.id;
    const newRoom = {
      id: product.id,
      sellerName: product.seller,
      productTitle: product.title,
      messages: [],
    };
    setChatRooms((prev) => [newRoom, ...prev]);
    return product.id;
  };

  const addBarterRequest = (request) => {
    setBarterRequests((prev) => [
      {
        id: Date.now(),
        status: "대기중",
        createdAt: new Date().toLocaleDateString(),
        ...request,
      },
      ...prev,
    ]);
  };

  const updateBarterStatus = (requestId, newStatus) => {
    setBarterRequests((prev) =>
      prev.map((req) =>
        req.id === requestId ? { ...req, status: newStatus } : req,
      ),
    );
  };

  // ── 로그인 (서버 실패 시 로컬 폴백) ──────────────
  const login = async (id, password) => {
    try {
      const res = await fetch("http://localhost:5002/api/auth/login", {
        method: "POST",
        headers: { "Content-Type": "application/json" },
        body: JSON.stringify({ email: id, password }),
      });
      const data = await res.json();
      if (res.ok) {
        localStorage.setItem("token", data.token);
        localStorage.setItem("nickname", data.nickname);
        localStorage.setItem("email", id);
        setCurrentUser({
          name: data.nickname,
          email: id,
          id: data.userId || id,
        });
        return { success: true };
      }
    } catch {
      console.warn("⚠️ 백엔드 연결 실패 → 로컬 모드");
    }

    const fallback = id.split("@")[0] || "테스트유저";
    localStorage.setItem("token", "local_bypass_token_9999");
    localStorage.setItem("nickname", fallback);
    localStorage.setItem("email", id);
    setCurrentUser({ name: fallback, email: id, id: "local_guest" });
    return { success: true, message: "로컬 안전 모드로 연결되었습니다." };
  };

  const register = async (newUser) => {
    try {
      const res = await fetch("http://localhost:5002/api/auth/signup", {
        method: "POST",
        headers: { "Content-Type": "application/json" },
        body: JSON.stringify({
          email: newUser.email,
          password: newUser.password,
          nickname: newUser.name,
        }),
      });
      if (!res.ok)
        return { success: false, message: (await res.json()).message };
      return { success: true };
    } catch {
      return { success: true, message: "로컬 안전 가입 완료" };
    }
  };

  const updateUserInfo = async (updatedData) => {
    const token = localStorage.getItem("token");
    if (!token) return { success: false, message: "로그인이 필요합니다." };
    try {
      const res = await fetch("http://localhost:5002/api/auth/update", {
        method: "PUT",
        headers: {
          "Content-Type": "application/json",
          Authorization: `Bearer ${token}`,
        },
        body: JSON.stringify(updatedData),
      });
      if (res.ok && updatedData.nickname) {
        localStorage.setItem("nickname", updatedData.nickname);
        setCurrentUser((prev) => ({ ...prev, name: updatedData.nickname }));
      }
    } catch {
      if (updatedData.nickname) {
        localStorage.setItem("nickname", updatedData.nickname);
        setCurrentUser((prev) => ({ ...prev, name: updatedData.nickname }));
      }
    }
    return { success: true };
  };

  const verifyPassword = async (password) => {
    const token = localStorage.getItem("token");
    if (!token) return { success: false };
    try {
      const res = await fetch(
        "http://localhost:5002/api/auth/verify-password",
        {
          method: "POST",
          headers: {
            "Content-Type": "application/json",
            Authorization: `Bearer ${token}`,
          },
          body: JSON.stringify({ password }),
        },
      );
      return { success: res.ok };
    } catch {
      return { success: true };
    }
  };

  const logout = () => {
    ["token", "nickname", "email", "local_likes"].forEach((k) =>
      localStorage.removeItem(k),
    );
    setCurrentUser(null);
    setLikedItems([]);
    setMyProducts([]);
  };

  // ── 필터링 ──────────────────────────────────────
  const filteredProducts = products.filter((product) => {
    const matchCategory =
      selectedCategory === "전체" || product.category === selectedCategory;
    const matchSearch = product.title
      .toLowerCase()
      .includes(searchQuery.toLowerCase());
    let matchRegion = true;
    if (regionFilter.sido !== "전국") {
      if (regionFilter.sigungu === "전체")
        matchRegion = product.location?.startsWith(regionFilter.sido);
      else if (regionFilter.dong === "전체")
        matchRegion = product.location?.startsWith(
          `${regionFilter.sido} ${regionFilter.sigungu}`,
        );
      else matchRegion = product.location?.includes(regionFilter.dong);
    }
    return matchCategory && matchSearch && matchRegion;
  });

  const likedProducts = products.filter((p) =>
    likedItems.includes(String(p.id)),
  );

  return (
    <AppContext.Provider
      value={{
        currentUser,
        setCurrentUser,
        login,
        logout,
        register,
        updateUserInfo,
        verifyPassword,
        products,
        filteredProducts,
        addProduct,
        myProducts,
        selectedCategory,
        setSelectedCategory,
        searchQuery,
        setSearchQuery,
        regionFilter,
        setRegionFilter,
        isHeaderMenuOpen,
        toggleHeaderMenu,
        myPoints,
        likedItems,
        toggleLike,
        likedProducts,
        chatRooms,
        startChat,
        barterRequests,
        addBarterRequest,
        updateBarterStatus,
      }}
    >
      {children}
    </AppContext.Provider>
  );
}

export const useApp = () => useContext(AppContext);
